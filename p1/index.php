      <h2>Brief Bio</h2>

      <p>Dr. Robelyn Garcia has been a youth pastor, ordained minister and professor since the 1990s. She became a minister with International Metaphysical Ministry in 2018 while completing her ninth college degree, a Bachelors in Metaphysical Science, through the University of Metaphysics.</p>

      <p>As a Multidisciplinary Post-Doc Scholar, Dr. Garcia’s mission is to educate and motivate! Her research transcends a myriad of disciplines. Through continued Metaphysical exploration, meditation research, implementation of real-world holistic health models, behavioral health coaching, spiritual counseling, telehealth modalities and biomimicry designs, her goal is to decrease the chronic and fatal health conditions associated with clinical obesity and geriatric depression. By utilizing and integrating the Metaphysical approach of self-improvement and self-development, Dr. Garcia creates a positive, nature-inspired wellness space for her clients and students. In her quest of a holistic approach for all endeavors, Dr. Garcia has began an educational journey as a Metaphysician with The University of Sedona and The University of Metaphysics.
      </p>

      <p>She is currently a Faculty Affiliate at Arizona State University, a Post-Doc Scholar at Harvard University Extension School, an American Red Cross Instructor at Senior University and a Private Professor at Personal Professors Online Academy. Dr. Garcia’s drive for educational excellence is fueled by the desire to help others with their overall physical, spiritual and behavioral health. She is also a former Professional Basketball Player, Kinesiology Professor, President Emeritus of the Jr. NBA-WNBA and Lifespan Professor. In 1996, after retiring from an All-Star professional championship basketball career, Dr. Garcia combined her love of the game with her passion for sharing the word of God as the Owner, Player and Coach for Christian Basketball Team Garcia.
      </p>
      <p>Dr. Garcia’s education is as diverse as her teaching. In addition to her 10 completed degrees, she just finished the coursework for her 2nd Doctorate in Behavioral Health at Arizona State University. She has also started her 11th college degree at Harvard University Extension School. The chronological break below reflects the years she spent as a Kinesiology Professor before beginning her pursuit to becoming a Multidisciplinary Professor, Health Educator and Interdisciplinary Post-Doc Scholar.
      </p>

